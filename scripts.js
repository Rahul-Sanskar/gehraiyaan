document.addEventListener('DOMContentLoaded', function() {
    const founderBtn = document.getElementById('founder-btn');
    const founderInfo = document.getElementById('founder-info');

    founderBtn.addEventListener('click', function() {
        if (founderInfo.style.display === 'none' || founderInfo.style.display === '') {
            founderInfo.style.display = 'block';
        } else {
            founderInfo.style.display = 'none';
        }
    });
});
